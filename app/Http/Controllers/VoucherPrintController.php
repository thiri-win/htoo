<?php

namespace App\Http\Controllers;

use App\Models\Voucher;
use Inertia\Inertia;

class VoucherPrintController extends Controller
{
    public function print(Voucher $voucher)
    {
        return Inertia::render('print/Voucher',[
            'voucher'=>$voucher->load('sales')
        ]);
    }
}
